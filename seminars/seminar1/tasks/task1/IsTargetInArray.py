# –ò–º–ø–µ—Ä–∞—Ç–∏–≤–Ω–∞—è –ø–∞—Ä–∞–¥–∏–≥–º–∞
def search_imperative(target, array):
    for num in array:
        if num == target:
            return True
    return False


print(search_imperative(4, [1, 2, 3, 4, 5, 6]))


# –î–µ–∫–ª–∞—Ä–∞—Ç–∏–≤–Ω–∞—è –ø–∞—Ä–∞–¥–∏–≥–º–∞
def search_declarative(target, array):
    return target in array


print(search_declarative(2, [1, 2, 3, 4, 5, 6]))
